let currentLang = localStorage.getItem('bs_lang') || 'ar';

function applyTranslations(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.body.dir = lang === 'ar' ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key] !== undefined) {
      el.innerHTML = translations[lang][key];
    }
  });

  const langLabel = document.getElementById('langToggleLabel');
  if (langLabel) langLabel.textContent = lang === 'ar' ? 'EN' : 'AR';

  localStorage.setItem('bs_lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations(currentLang);

  const langToggle = document.getElementById('langToggle');
  if (langToggle) {
    langToggle.addEventListener('click', () => {
      applyTranslations(currentLang === 'ar' ? 'en' : 'ar');
    });
  }

  const langToggleSector = document.getElementById('langToggleSector');
  if (langToggleSector) {
    langToggleSector.addEventListener('click', () => {
      applyTranslations(currentLang === 'ar' ? 'en' : 'ar');
    });
  }
});
